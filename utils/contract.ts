export const getContract = (signer: ethers.ContractRunner | null | undefined) => {
	return new ethers.Contract(CONTRACT_ADDRESS, CONTRACT_ABI , signer);
  };

export const publishCourse = async (signer: ethers.Signer, price: bigint, contentURI: string) => {
	const contract = getContract(signer);
	const tx = await contract.publishCourse(price, contentURI);
	await tx.wait();
	return tx;
};


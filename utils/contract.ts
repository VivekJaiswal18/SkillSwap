export const getAllCourses = async () => {
	const provider = new ethers.JsonRpcProvider(lineasepoliachain.rpcUrls.default.http[0]);
	const contract = getContract(provider);
	const courseCount = await contract.nextCourseId();
	
	const courses = [];
	for (let i = 1; i < courseCount; i++) {
	  const course = await contract.courses(i);
	  courses.push({
		id: i,
		creator: course.creator,
		price: course.price,
		isActive: course.isActive,
		contentURI: course.contentURI
	  });
	}
	console.log("Fetched courses:", courses);
	
	return courses;
  };

  export const purchaseCourse = async (signer: ethers.Signer, courseId: number, price: bigint) => {
	const contract = getContract(signer);
	const tx = await contract.purchaseCourse(courseId, { value: price });
	await tx.wait();
	return tx;
  };